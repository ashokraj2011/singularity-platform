#!/usr/bin/env bash
# Point npm/pnpm installs at the office Artifactory npm registry.
#
# Reads the registry + token from env and writes a .npmrc into the repo root
# (and, with --all, into every Node workspace that has a package.json). The
# token is interpolated by npm/pnpm at runtime, so we DON'T bake it into the
# file — we write ${ARTIFACTORY_NPM_TOKEN} literally and rely on the shell env
# at install time. (Pass --inline to write the resolved token instead, e.g.
# for a throwaway CI layer — never commit that.)
#
# Required env:
#   ARTIFACTORY_NPM_REGISTRY  e.g. https://art.co/artifactory/api/npm/npm-virtual/
#   ARTIFACTORY_NPM_TOKEN     Artifactory identity token (or base64 user:pass)
#
# Usage:
#   ARTIFACTORY_NPM_REGISTRY=... ARTIFACTORY_NPM_TOKEN=... bin/use-artifactory.sh [--all] [--inline]
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
ALL=false
INLINE=false
for arg in "$@"; do
  case "$arg" in
    --all)    ALL=true ;;
    --inline) INLINE=true ;;
    *) echo "unknown arg: $arg" >&2; exit 2 ;;
  esac
done

: "${ARTIFACTORY_NPM_REGISTRY:?set ARTIFACTORY_NPM_REGISTRY (e.g. https://art.co/artifactory/api/npm/npm-virtual/)}"
: "${ARTIFACTORY_NPM_TOKEN:?set ARTIFACTORY_NPM_TOKEN (Artifactory identity token or base64 user:pass)}"

# Derive the //host/path used by npm for auth from the registry URL.
# https://art.co/artifactory/api/npm/npm-virtual/  ->  art.co/artifactory/api/npm/npm-virtual/
auth_path="$(printf '%s' "$ARTIFACTORY_NPM_REGISTRY" | sed -E 's#^https?://##')"

if $INLINE; then
  token_line="//${auth_path}:_authToken=${ARTIFACTORY_NPM_TOKEN}"
  registry_line="registry=${ARTIFACTORY_NPM_REGISTRY}"
else
  # Literal ${...} so npm/pnpm interpolate from env at install time.
  token_line='//'"${auth_path}"':_authToken=${ARTIFACTORY_NPM_TOKEN}'
  registry_line='registry=${ARTIFACTORY_NPM_REGISTRY}'
fi

write_npmrc() {
  local dir="$1"
  cat > "$dir/.npmrc" <<EOF
# Generated by bin/use-artifactory.sh — points installs at Artifactory.
# Do NOT commit if it contains a real token (--inline mode). Env-var mode is
# safe to keep local; it's already covered by .gitignore.
${registry_line}
${token_line}
fetch-retries=3
fetch-retry-factor=2
fetch-retry-mintimeout=10000
fetch-retry-maxtimeout=60000
audit=false
fund=false
EOF
  echo "wrote $dir/.npmrc"
}

write_npmrc "$ROOT"

if $ALL; then
  # Every workspace that runs its own install (has a package.json + is a build
  # context). Skip node_modules and dist.
  while IFS= read -r pkg; do
    write_npmrc "$(dirname "$pkg")"
  done < <(find "$ROOT" -name package.json \
            -not -path '*/node_modules/*' -not -path '*/dist/*' \
            -not -path '*/.git/*' | sort)
fi

echo
echo "Done. npm/pnpm will now resolve from: ${ARTIFACTORY_NPM_REGISTRY}"
$INLINE && echo "WARNING: --inline wrote the literal token into .npmrc — do not commit." || \
  echo "Token is read from \$ARTIFACTORY_NPM_TOKEN at install time (not stored)."
